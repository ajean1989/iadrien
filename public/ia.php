        <h1 id=<?php $ini=0; echo $ini ; $ini++ ;?>><a href="./../documents/RERP_Story_3_-_Introduction_IA.pdf" target="_blank">Technologies</a></h1>

        <p>
            <ul>
                <li><strong>Machine learning (apprentissage automatique)</strong> : Technologie qui utilise des données pour apprendre seul et inférer des modèles d’application optimaux dans un domaine spécifique. C’est la technologie la plus ancienne et la plus simple. Elle s’appuie sur un algorithme qui adapte lui-même le système à partir des retours faits par l’humain. La mise en place de cette technologie implique l’existence de données organisées. Le système est ensuite alimenté par des données structurées et catégorisées lui permettant de comprendre comment classer de nouvelles données similaires.</li>
                <li><strong>Deep learning (apprentissage profond)</strong> : Le Deep learning est une technologie inclue dans le machine learning. Technologie qui n’a pas besoin de données structurées et qui utilise des réseaux de neurones multicouches pour apprendre et catégoriser de manière autonome les données.La profondeur est lié aux couches cachées de ses réseaux neuronaux.</li>
            </ul>
        </p>

        <p>
            <ul>
                <li><strong>Modèle</strong> : Fichier qui a fait l’objet d’un traitement IA préalable (soumis à une base d’apprentissage) et qui peut être utilisé pour automatiser certaines tâches.</li>
                <li><strong>Système expert</strong> : Programme capable de se comporter comme un humain sur une tâche donnée.</li>
                <li><strong>Réseau de neurones</strong> : Modèle dont la structure est identique au réseau de neurone du cerveau humain pour apprendre et reconnaitre. Utilisé en deep learning mais pas que, il s’agit d’un concept.Ils sont efficaces dans la modélisation des problèmes complexes et non linéaires. </li>
            </ul>
        </p>

        <p>
            <ul>
                <li><strong>Apprentissage supervisé</strong> : L’algorithme utilise un très grand jeu de données étiquetées (on lui dit les donne les réponses qu’on attend) soit pour classer un objet dans un catégorie spécifiée (classification), ou attribuer une valeur à un objet (régression).</li>
                <li><strong>Apprentissage non-supervisé</strong> : Programme qui utilise un très grand jeu de données non étiquetées et créé ses propres réponses grâce au clustering (Comme la classification mais les classes sont inventées par le programme) ou au filtrage collaboratif (recommandation).</li>
                <li><strong>Apprentissage semi-supervisé</strong> : Cet algorithme utilise une petite quantité de données étiquetées comme le machine learning supervisé et une plus grande quantité de données non étiquetées comme le machine learning non supervisé pour entraîner les algorithmes. Les données étiquetées peuvent être utilisées pour entraîner partiellement l’algorithme de machine learning, et cet algorithme partiellement entraîné trouve également des informations de manière organique.</li>
                <li><strong>Apprentissage renforcé (reinforcement learning)</strong> : Le programme apprend par expérience en optimisant une récompense dans le temps. </li>
            </ul>
        </p>

        <p>
            <ul>
                <li><strong>Data mining</strong> : Technique IA - Il s’agit d’extraire une connaissance (lien entre plusieurs informations - modèle - patterns) à partir d’une grande quantité de données.</li>
                <li><strong>Classification</strong> : Technique de machine learning supervisée qui permet de classer des objets dans des classes(labels/groupe) prédéfinies. → 2 classes ou plus.</li>
                <li><strong>Régression linéaire</strong> : Technique de machine learning supervisée qui permet de prédire une valeur réelle/continue à partir des entrées. → La régression logistique est une classification.</li>
                <li><strong>Clustering</strong> : Technique permettant grâce à l’apprentissage non supervisé de trouver des modèles ou des groupements dans les données</li>
            </ul>
        </p>







